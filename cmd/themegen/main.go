func writeThemeRegisters(themesDir, outDir, module, appVersion string) (int, error) {
	entries, err := os.ReadDir(themesDir)
	if err != nil {
		if os.IsNotExist(err) {
			return 0, nil
		}

		return 0, fmt.Errorf("read themes dir %s: %w", themesDir, err)
	}

	count := 0
	for _, entry := range entries {
		if !entry.IsDir() {
			continue
		}

		wrote, err := writeThemeRegister(themesDir, outDir, entry.Name(), module, appVersion)
		if err != nil {
			return count, err
		}
		if wrote {
			count++
		}
	}

	return count, nil
}

func writeThemeRegister(themesDir, outDir, themeName, module, appVersion string) (bool, error) {
	_, proceed, err := gateThemeCompat(themesDir, outDir, themeName, appVersion)
	if err != nil {
		return false, err
	}
	if !proceed {
		return false, nil
	}

	themeDir := filepath.Join(themesDir, themeName)
	httpxDir := filepath.Join(themeDir, "platform", "httpx")
	info, err := os.Stat(httpxDir)
	if err != nil {
		if os.IsNotExist(err) {
			return false, nil
		}

		return false, fmt.Errorf("stat %s: %w", httpxDir, err)
	}
	if !info.IsDir() {
		return false, nil
	}

	overrides, err := discoverPlatformOverrides(httpxDir)
	if err != nil {
		return false, fmt.Errorf("discover overrides %s: %w", themeName, err)
	}

	if len(overrides) == 0 {
		return false, nil
	}

	if err := writeRegister(themeName, httpxDir, module, overrides); err != nil {
		return false, fmt.Errorf("write register %s: %w", themeName, err)
	}

	return true, nil
}

func emitActiveFallback(themeName, appVersion, themeMin string) string {
	var b strings.Builder

	b.WriteString(generatedHeader)
	fmt.Fprintf(&b, "//go:build theme_%s\n\n", themeName)
	b.WriteString("package theme\n\n")
	b.WriteString("import \"log/slog\"\n\n")
	b.WriteString("func init() {\n")
	b.WriteString("\tslog.Warn(\"theme: requested theme not compatible with app version, falling back to default\",\n")
	fmt.Fprintf(&b, "\t\t\"theme\", %q,\n", themeName)
	fmt.Fprintf(&b, "\t\t\"theme_min\", %q,\n", themeMin)
	fmt.Fprintf(&b, "\t\t\"app_version\", %q,\n", appVersion)
	b.WriteString("\t)\n")
	b.WriteString("}\n")

	return b.String()
}

func writeActiveFallback(outDir, themeName, appVersion string, mf manifest.Manifest) error {
	source := emitActiveFallback(themeName, appVersion, mf.Compatible.Min)
	outPath := filepath.Join(outDir, "active_"+themeName+"_gen.go")
	return writeIfChanged(outPath, []byte(source))
}

