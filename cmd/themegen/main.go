const (
	generatedHeader = "// Code generated by themegen. DO NOT EDIT.\n\n"
	dirPerm         = 0o750
	filePerm        = 0o600

	templFileType = "templ"
	mdFileType    = "md"

	defaultThemeName = "default"
)

var (
	templFuncRe = regexp.MustCompile(`(?m)^templ\s+([A-Z][A-Za-z0-9_]*)\s*\(([^)]*)\)`)
	nonIdentRe  = regexp.MustCompile(`[^a-zA-Z0-9]`)
	capIdentRe  = regexp.MustCompile(`(^|[^.\w])([A-Z][A-Za-z0-9_]*)`)

	importBlockRe   = regexp.MustCompile(`(?s)import\s*\(([^)]+)\)`)
	importLineRe    = regexp.MustCompile(`(?m)^\s*(?:(\w+)\s+)?"([^"]+)"`)
	qualifiedTypeRe = regexp.MustCompile(`\b([a-z][a-zA-Z0-9_]*)\.[A-Z]\w*`)

	skippedDirSegments = map[string]struct{}{
		"internal":  {},
		"features":  {},
		"platform":  {},
		"transport": {},
	}

	platformOverrideable = []string{"Base", "Page404", "Page429"}
	platformVarFor       = map[string]string{
		"Base":    "ActiveBase",
		"Page404": "ActivePage404",
		"Page429": "ActivePage429",
	}
)

type Component struct {
	Method       string
	ImportPath   string
	ImportName   string
	FuncName     string
	Params       string
	Args         string
	ExtraImports []importEntry
}

type importEntry struct {
	Alias string
	Path  string
}

//nolint:cyclop // codegen entry point
func main() {
	roots := flag.String("roots", "themes/default/features,internal/platform/httpx", "comma-separated directories to scan for .templ files")
	out := flag.String("out", "internal/platform/theme", "output directory for generated files")
	themesDir := flag.String("themes", "themes", "directory containing theme subdirectories")
	module := flag.String("module", "github.com/hayakawakaki/go-racp", "Go module path used to build import paths")
	flag.Parse()

	rootList := []string{}
	for r := range strings.SplitSeq(*roots, ",") {
		r = strings.TrimSpace(r)
		if r != "" {
			rootList = append(rootList, r)
		}
	}

	discovered, err := scan(rootList, *module)
	if err != nil {
		log.Fatalf("themegen: scan: %v", err)
	}

	if err = os.MkdirAll(*out, dirPerm); err != nil {
		log.Fatalf("themegen: mkdir: %v", err)
	}

	if err = writeInterface(*out, discovered); err != nil {
		log.Fatalf("themegen: write interface: %v", err)
	}

	if err = writeDefault(*out, discovered); err != nil {
		log.Fatalf("themegen: write default: %v", err)
	}

	if err = writeThemeOverrides(*themesDir, *out, *module, discovered); err != nil {
		log.Fatalf("themegen: write theme overrides: %v", err)
	}

	if err = writeActiveDefault(*out, *module); err != nil {
		log.Fatalf("themegen: write active_default: %v", err)
	}

	appVersion, err := readAppVersion()
	if err != nil {
		log.Fatalf("themegen: read app version: %v", err)
	}

	registers, err := writeThemeRegisters(*themesDir, *out, *module, appVersion)
	if err != nil {
		log.Fatalf("themegen: write theme registers: %v", err)
	}

	pages, err := writeAllThemePages(*themesDir, *module)
	if err != nil {
		log.Fatalf("themegen: write theme pages: %v", err)
	}

	configs, err := writeAllThemeConfigs(*themesDir, "internal/platform/themecfg")
	if err != nil {
		log.Fatalf("themegen: write theme configs: %v", err)
	}

	fmt.Printf("themegen: %d components, %d theme registers, %d theme page tables, %d theme configs\n", len(discovered), registers, pages, configs)

	if len(changedFiles) == 0 {
		fmt.Println("themegen: no files changed")
		return
	}

	fmt.Printf("themegen: %d files changed:\n", len(changedFiles))

	for _, f := range changedFiles {
		fmt.Printf("  %s\n", f)
	}
}

func readAppVersion() (string, error) {
	data, err := os.ReadFile("internal/platform/version/VERSION") //nolint:gosec // fixed path
	if err != nil {
		return "", fmt.Errorf("read app version: %w", err)
	}

	return strings.TrimSpace(string(data)), nil
}

func readManifest(themeDir string) (manifest.Manifest, error) {
	data, err := os.ReadFile(filepath.Join(themeDir, "theme.yml")) //nolint:gosec // path from caller
	if err != nil {
		return manifest.Manifest{}, fmt.Errorf("read %s/theme.yml: %w", themeDir, err)
	}

	m, err := manifest.ParseManifest(data)
	if err != nil {
		return manifest.Manifest{}, fmt.Errorf("parse %s/theme.yml: %w", themeDir, err)
	}

	return m, nil
}

func scan(roots []string, module string) ([]Component, error) {
	var out []Component
	for _, root := range roots {
		err := filepath.WalkDir(root, func(path string, d os.DirEntry, err error) error {
			if err != nil {
				return fmt.Errorf("walk %s: %w", path, err)
			}
			if d.IsDir() {
				return nil
			}
			if !strings.HasSuffix(path, ".templ") {
				return nil
			}

			norm := filepath.ToSlash(path)
			if strings.HasPrefix(norm, "themes/default/features/") && !strings.Contains(norm, "/transport/") {
				return nil
			}

			comps, err := extract(path, module)
			if err != nil {
				return fmt.Errorf("%s: %w", path, err)
			}
			out = append(out, comps...)

			return nil
		})
		if err != nil {
			return nil, fmt.Errorf("scan %s: %w", root, err)
		}
	}

	return out, nil
}

func extract(path, module string) ([]Component, error) {
	data, err := os.ReadFile(path) //nolint:gosec // path from WalkDir
	if err != nil {
		return nil, fmt.Errorf("read %s: %w", path, err)
	}
	src := string(data)

	importPath := module + "/" + filepath.ToSlash(filepath.Dir(path))
	importName := aliasFor(filepath.ToSlash(filepath.Dir(path)))
	fileImports := parseTemplImports(src)

	var out []Component
	for _, m := range templFuncRe.FindAllStringSubmatch(src, -1) {
		funcName := m[1]
		rawParams := strings.TrimSpace(m[2])
		params := qualifyTypes(rawParams, importName)
		args := paramNames(rawParams)
		extras := referencedImports(rawParams, fileImports)
		out = append(out, Component{
			Method:       funcName,
			ImportPath:   importPath,
			ImportName:   importName,
			FuncName:     funcName,
			Params:       params,
			Args:         args,
			ExtraImports: extras,
		})
	}

	return out, nil
}

func parseTemplImports(src string) []importEntry {
	block := importBlockRe.FindStringSubmatch(src)
	if len(block) < 2 {
		return nil
	}

	var out []importEntry
	for _, m := range importLineRe.FindAllStringSubmatch(block[1], -1) {
		alias := m[1]
		path := m[2]
		if alias == "" {
			parts := strings.Split(path, "/")
			alias = parts[len(parts)-1]
		}
		out = append(out, importEntry{Alias: alias, Path: path})
	}

	return out
}

func referencedImports(params string, fileImports []importEntry) []importEntry {
	if len(fileImports) == 0 {
		return nil
	}

	byAlias := map[string]importEntry{}
	for _, imp := range fileImports {
		byAlias[imp.Alias] = imp
	}

	seen := map[string]struct{}{}
	var out []importEntry
	for _, m := range qualifiedTypeRe.FindAllStringSubmatch(params, -1) {
		alias := m[1]
		if _, ok := seen[alias]; ok {
			continue
		}
		imp, found := byAlias[alias]
		if !found {
			continue
		}
		seen[alias] = struct{}{}
		out = append(out, imp)
	}

	return out
}

func qualifyTypes(params, localAlias string) string {
	return capIdentRe.ReplaceAllStringFunc(params, func(match string) string {
		sub := capIdentRe.FindStringSubmatch(match)
		return sub[1] + localAlias + "." + sub[2]
	})
}

func aliasFor(dir string) string {
	parts := strings.Split(dir, "/")

	if len(parts) >= 2 && parts[0] == "themes" {
		parts = parts[2:]
	}

	var keep []string
	for _, p := range parts {
		if _, skip := skippedDirSegments[p]; skip {
			continue
		}
		keep = append(keep, p)
	}
	if len(keep) == 0 {
		keep = parts
	}

	return nonIdentRe.ReplaceAllString(strings.Join(keep, ""), "")
}

func paramNames(params string) string {
	if params == "" {
		return ""
	}

	var names []string
	for p := range strings.SplitSeq(params, ",") {
		fields := strings.Fields(strings.TrimSpace(p))
		if len(fields) == 0 {
			continue
		}
		names = append(names, fields[0])
	}

	return strings.Join(names, ", ")
}

func uniqueImports(comps []Component) ([]importEntry, error) {
	seen := map[string]string{}
	for _, c := range comps {
		if existing, ok := seen[c.ImportName]; ok && existing != c.ImportPath {
			return nil, fmt.Errorf("alias collision: %q maps to both %q and %q", c.ImportName, existing, c.ImportPath)
		}
		seen[c.ImportName] = c.ImportPath
		for _, extra := range c.ExtraImports {
			if existing, ok := seen[extra.Alias]; ok && existing != extra.Path {
				return nil, fmt.Errorf("alias collision: %q maps to both %q and %q", extra.Alias, existing, extra.Path)
			}
			seen[extra.Alias] = extra.Path
		}
	}

	out := make([]importEntry, 0, len(seen))
	for alias, path := range seen {
		out = append(out, importEntry{Alias: alias, Path: path})
	}
	slices.SortFunc(out, func(a, b importEntry) int {
		return cmp.Compare(a.Path, b.Path)
	})

	return out, nil
}

var changedFiles []string

func writeIfChanged(path string, content []byte) error {
	existing, err := os.ReadFile(path) //nolint:gosec // path from caller
	if err == nil && bytes.Equal(existing, content) {
		return nil
	}

	if err := os.WriteFile(path, content, filePerm); err != nil {
		return fmt.Errorf("write %s: %w", path, err)
	}

	changedFiles = append(changedFiles, path)

	return nil
}

func formatImport(imp importEntry) string {
	parts := strings.Split(imp.Path, "/")
	last := parts[len(parts)-1]
	if imp.Alias == last {
		return fmt.Sprintf("\t%q\n", imp.Path)
	}

	return fmt.Sprintf("\t%s %q\n", imp.Alias, imp.Path)
}

func writeInterface(outDir string, comps []Component) error {
	imports, err := uniqueImports(comps)
	if err != nil {
		return fmt.Errorf("collect imports: %w", err)
	}

	var paramsCat strings.Builder
	for _, c := range comps {
		paramsCat.WriteString(c.Params)
		paramsCat.WriteString(" ")
	}
	used := paramsCat.String()

	var b strings.Builder
	b.WriteString(generatedHeader)
	b.WriteString("package theme\n\n")
	b.WriteString("import (\n")
	b.WriteString("\t\"github.com/a-h/templ\"\n")
	for _, imp := range imports {
		if !strings.Contains(used, imp.Alias+".") {
			continue
		}
		b.WriteString(formatImport(imp))
	}
	b.WriteString(")\n\n")
	b.WriteString("type Theme interface {\n")
	for _, c := range comps {
		fmt.Fprintf(&b, "\t%s(%s) templ.Component\n", c.Method, c.Params)
	}
	b.WriteString("}\n")

	return writeIfChanged(filepath.Join(outDir, "interface_gen.go"), []byte(b.String()))
}

func writeDefault(outDir string, comps []Component) error {
	imports, err := uniqueImports(comps)
	if err != nil {
		return fmt.Errorf("collect imports: %w", err)
	}

	var b strings.Builder
	b.WriteString(generatedHeader)
	b.WriteString("package theme\n\n")
	b.WriteString("import (\n")
	b.WriteString("\t\"github.com/a-h/templ\"\n")
	for _, imp := range imports {
		b.WriteString(formatImport(imp))
	}
	b.WriteString(")\n\n")
	b.WriteString("type DefaultTheme struct{}\n\n")
	b.WriteString("var _ Theme = DefaultTheme{}\n\n")
	for _, c := range comps {
		fmt.Fprintf(&b, "func (DefaultTheme) %s(%s) templ.Component {\n", c.Method, c.Params)
		fmt.Fprintf(&b, "\treturn %s.%s(%s)\n", c.ImportName, c.FuncName, c.Args)
		b.WriteString("}\n\n")
	}

	return writeIfChanged(filepath.Join(outDir, "default_gen.go"), []byte(b.String()))
}

func emitThemeOverride(themeName string, overrides []Component) string {
	var b strings.Builder

	b.WriteString(generatedHeader)
	fmt.Fprintf(&b, "//go:build theme_%s\n\n", themeName)
	b.WriteString("package theme\n\n")

	imports, err := uniqueImports(overrides)
	if err != nil {
		panic(fmt.Sprintf("themegen: collect imports for %s: %v", themeName, err))
	}

	b.WriteString("import (\n")
	b.WriteString("\t\"github.com/a-h/templ\"\n")
	for _, imp := range imports {
		b.WriteString(formatImport(imp))
	}
	b.WriteString(")\n\n")

	structName := pascalCase(themeName) + "Theme"
	fmt.Fprintf(&b, "type %s struct{ DefaultTheme }\n\n", structName)
	fmt.Fprintf(&b, "var _ Theme = %s{}\n\n", structName)

	for _, c := range overrides {
		fmt.Fprintf(&b, "func (%s) %s(%s) templ.Component {\n", structName, c.Method, c.Params)
		fmt.Fprintf(&b, "\treturn %s.%s(%s)\n", c.ImportName, c.FuncName, c.Args)
		b.WriteString("}\n\n")
	}

	return b.String()
}

func pascalCase(s string) string {
	if s == "" {
		return ""
	}

	parts := strings.FieldsFunc(s, func(r rune) bool {
		return r == '_' || r == '-'
	})

	var b strings.Builder
	for _, p := range parts {
		if p == "" {
			continue
		}
		b.WriteString(strings.ToUpper(p[:1]))
		if len(p) > 1 {
			b.WriteString(p[1:])
		}
	}

	return b.String()
}

