//nolint:cyclop // per-theme presence checks
func writeAllThemePages(themesDir, module string) (int, error) {
	entries, err := os.ReadDir(themesDir)
	if err != nil {
		if os.IsNotExist(err) {
			return 0, nil
		}

		return 0, fmt.Errorf("read themes dir %s: %w", themesDir, err)
	}

	count := 0
	allPages := []pageEntry{}

	for _, entry := range entries {
		if !entry.IsDir() {
			continue
		}

		themeName := entry.Name()
		themeDir := filepath.Join(themesDir, themeName)

		accessPath := filepath.Join(themeDir, "access.yml")

		if _, err := os.Stat(accessPath); err != nil {
			return count, fmt.Errorf("theme %s: missing access.yml at %s, every theme requires this file (may be empty except for header comment)", themeName, accessPath)
		}

		embedPath := filepath.Join(themeDir, "access_embed.go")

		if _, err := os.Stat(embedPath); err != nil {
			return count, fmt.Errorf("theme %s: missing access_embed.go at %s, every theme requires this file to embed access.yml", themeName, embedPath)
		}

		pagesDir := filepath.Join(themeDir, "pages")
		info, err := os.Stat(pagesDir)
		if err != nil || !info.IsDir() {
			continue
		}

		pages, err := collectThemePages(pagesDir)
		if err != nil {
			return count, fmt.Errorf("theme %s: %w", themeName, err)
		}

		if err = writeThemePages(themeDir, themeName, module, pages); err != nil {
			return count, fmt.Errorf("theme %s: write pages_gen: %w", themeName, err)
		}

		mf, err := readManifest(themeDir)
		if err != nil {
			return count, fmt.Errorf("theme %s: %w", themeName, err)
		}

		if err = writeThemeManifestGen(themeDir, themeName, mf, len(pages)); err != nil {
			return count, fmt.Errorf("theme %s: write manifest_gen: %w", themeName, err)
		}

		allPages = append(allPages, pages...)
		count++
	}

	if len(allPages) > 0 {
		fmt.Println("themegen: theme pages registered:")

		for _, p := range allPages {
			fmt.Printf("  %s -> %s\n", p.Tag, p.Route)
		}
	}

	return count, nil
}

//nolint:cyclop // file-to-route mapping
func collectThemePages(pagesDir string) ([]pageEntry, error) {
	var out []pageEntry

	seenRoutes := map[string]string{}

	err := filepath.WalkDir(pagesDir, func(path string, d os.DirEntry, walkErr error) error {
		if walkErr != nil {
			return fmt.Errorf("walk %s: %w", path, walkErr)
		}

		base := filepath.Base(path)

		if d.IsDir() {
			if path == pagesDir {
				return nil
			}

			if strings.HasPrefix(base, "_") {
				return filepath.SkipDir
			}

			return nil
		}

		if strings.HasPrefix(base, "_") || base == ".keep" {
			return nil
		}

		ext := filepath.Ext(base)

		var kind string
		switch ext {
		case ".templ":
			kind = templFileType
		case ".md":
			kind = mdFileType
		default:
			return nil
		}

		if strings.ContainsAny(base, "[]") {
			return fmt.Errorf("page %s: dynamic segments not supported", path)
		}

		rel, err := filepath.Rel(pagesDir, path)
		if err != nil {
			return fmt.Errorf("rel %s: %w", path, err)
		}

		rel = filepath.ToSlash(rel)
		relNoExt := strings.TrimSuffix(rel, ext)

		route := "/" + relNoExt
		route = strings.TrimSuffix(route, "/index")

		if route == "" {
			route = "/"
		}

		if !pageRoutePathRe.MatchString(strings.TrimPrefix(route, "/")) && route != "/" {
			return fmt.Errorf("page %s: route %q has invalid characters", path, route)
		}

		if prev, ok := seenRoutes[route]; ok {
			return fmt.Errorf("page route collision: %s and %s both map to %q", prev, path, route)
		}

		seenRoutes[route] = path

		funcName := pascalCaseFromPath(relNoExt)
		title := titleFromPath(relNoExt)
		filePath := "pages/" + rel

		var varName string

		if kind == templFileType {
			data, err := os.ReadFile(path) //nolint:gosec // path from WalkDir
			if err != nil {
				return fmt.Errorf("read %s: %w", path, err)
			}

			if !containsTemplFunc(string(data), funcName) {
				return fmt.Errorf("page %s: expected templ function %q", path, funcName)
			}
		}

		if kind == mdFileType {
			data, err := os.ReadFile(path) //nolint:gosec // path from WalkDir
			if err != nil {
				return fmt.Errorf("read %s: %w", path, err)
			}

			fm, body, _ := parseFrontmatter(data)

			resolved := strings.TrimSpace(fm.Title)
			if resolved == "" {
				resolved = extractH1Title(body)
			}

			if resolved != "" {
				title = resolved
			}

			varName = pascalCaseVarName(relNoExt)
		}

		tag := themePageTag(relNoExt)
		suffix := tag[len("ThemePages."):]

		if suffix == "" {
			return fmt.Errorf("page %s: filename normalizes to an empty tag suffix, rename the file so it contains letters or digits", path)
		}

		first := suffix[0]

		if first < 'A' || first > 'Z' {
			return fmt.Errorf("page %s: derived tag %q starts with a non-letter, rename the file so it begins with a letter (a-z)", path, tag)
		}

		out = append(out, pageEntry{
			Route:    route,
			FilePath: filePath,
			Kind:     kind,
			FuncName: funcName,
			Title:    title,
			VarName:  varName,
			Tag:      tag,
		})

		return nil
	})
	if err != nil {
		return nil, fmt.Errorf("walk pages: %w", err)
	}

	seenTags := map[string]string{}

	for _, p := range out {
		if existing, dup := seenTags[p.Tag]; dup {
			return nil, fmt.Errorf("theme pages %s and %s both derive tag %s, rename one so their tags differ", existing, p.FilePath, p.Tag)
		}

		seenTags[p.Tag] = p.FilePath
	}

	slices.SortFunc(out, func(a, b pageEntry) int {
		return cmp.Compare(a.Route, b.Route)
	})

	return out, nil
}

func containsTemplFunc(src, funcName string) bool {
	for _, m := range pageTemplFuncRe.FindAllStringSubmatch(src, -1) {
		if m[1] == funcName {
			return true
		}
	}

	return false
}

func pascalCaseFromPath(relNoExt string) string {
	parts := strings.Split(relNoExt, "/")

	var b strings.Builder
	for _, p := range parts {
		for word := range strings.FieldsFuncSeq(p, func(r rune) bool {
			return r == '-' || r == '_'
		}) {
			if word == "" {
				continue
			}

			b.WriteString(strings.ToUpper(word[:1]))
			if len(word) > 1 {
				b.WriteString(word[1:])
			}
		}
	}

	return b.String()
}

func titleFromPath(relNoExt string) string {
	parts := strings.Split(relNoExt, "/")
	last := parts[len(parts)-1]

	if last == "" {
		return "Home"
	}

	var b strings.Builder
	first := true

	for word := range strings.FieldsFuncSeq(last, func(r rune) bool {
		return r == '-' || r == '_'
	}) {
		if word == "" {
			continue
		}

		if !first {
			b.WriteString(" ")
		}

		first = false
		b.WriteString(strings.ToUpper(word[:1]))
		if len(word) > 1 {
			b.WriteString(word[1:])
		}
	}

	return b.String()
}

//nolint:cyclop // code emission with branches per page kind
func writeThemePages(themeDir, themeName, module string, pages []pageEntry) error {
	pkgName := "themes" + themeName

	var b strings.Builder
	b.WriteString(generatedHeader)
	fmt.Fprintf(&b, "package %s\n\n", pkgName)

	hasTempl := false
	hasMD := false

	for _, p := range pages {
		if p.Kind == templFileType {
			hasTempl = true
		}

		if p.Kind == mdFileType {
			hasMD = true
		}
	}

	needsTheme := hasTempl || hasMD

	b.WriteString("import (\n")

	if hasMD {
		b.WriteString("\t\"embed\"\n")
		b.WriteString("\t\"html/template\"\n")
	}

	b.WriteString("\t\"net/http\"\n")

	if needsTheme {
		b.WriteString("\n")
		fmt.Fprintf(&b, "\t%q\n", module+"/internal/platform/httpx")
		fmt.Fprintf(&b, "\t%q\n", module+"/internal/platform/routes")
		fmt.Fprintf(&b, "\t%q\n", module+"/internal/platform/themepage")
	} else {
		b.WriteString("\n")
		fmt.Fprintf(&b, "\t%q\n", module+"/internal/platform/httpx")
		fmt.Fprintf(&b, "\t%q\n", module+"/internal/platform/routes")
	}

	if hasTempl {
		fmt.Fprintf(&b, "\tpages %q\n", module+"/themes/"+themeName+"/pages")
	}

	b.WriteString(")\n\n")

	if hasMD {
		b.WriteString("var (\n")

		for _, p := range pages {
			if p.Kind != mdFileType {
				continue
			}

			fmt.Fprintf(&b, "\tprecomputedHTML%s template.HTML\n", p.VarName)
		}

		b.WriteString(")\n\n")

		b.WriteString("func init() {\n")
		b.WriteString("\tvar err error\n\n")

		for _, p := range pages {
			if p.Kind != mdFileType {
				continue
			}

			fmt.Fprintf(&b, "\tif precomputedHTML%s, err = themepage.RenderMarkdown(mustRead(Pages, %q)); err != nil {\n", p.VarName, p.FilePath)
			fmt.Fprintf(&b, "\t\tpanic(%q + err.Error())\n", pkgName+": pre-render "+p.FilePath+": ")
			b.WriteString("\t}\n")
		}

		b.WriteString("}\n\n")

		b.WriteString("func mustRead(fs embed.FS, path string) []byte {\n")
		b.WriteString("\tdata, err := fs.ReadFile(path)\n")
		b.WriteString("\tif err != nil {\n")
		fmt.Fprintf(&b, "\t\tpanic(%q + path + %q + err.Error())\n", pkgName+": embed read ", ": ")
		b.WriteString("\t}\n\n")
		b.WriteString("\treturn data\n")
		b.WriteString("}\n\n")
	}

	if len(pages) == 0 {
		b.WriteString("func MountRoutes(_ *routes.Registry, _ *http.ServeMux, _ httpx.Layout) {\n")
		b.WriteString("}\n")
	} else {
		fmt.Fprintf(&b, "func MountRoutes(reg *routes.Registry, mux *http.ServeMux, layout httpx.Layout) {\n")
		for _, p := range pages {
			pattern := p.Route
			if pattern == "/" {
				pattern = "GET /{$}"
			}

			switch p.Kind {
			case templFileType:
				fmt.Fprintf(&b, "\treg.Wrap(mux, %q, %q, http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {\n", p.Tag, pattern)
				fmt.Fprintf(&b, "\t\tif err := pages.%s(themepage.BuildCtx(r, layout)).Render(r.Context(), w); err != nil {\n", p.FuncName)
				fmt.Fprintf(&b, "\t\t\thttp.Error(w, %q, http.StatusInternalServerError)\n", "render error")
				fmt.Fprintf(&b, "\t\t}\n")
				fmt.Fprintf(&b, "\t}))\n")
			case mdFileType:
				fmt.Fprintf(&b, "\treg.Wrap(mux, %q, %q, http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {\n", p.Tag, pattern)
				fmt.Fprintf(&b, "\t\tif err := themepage.RenderMarkdownPageFrom(w, r, layout, %q, %q, precomputedHTML%s); err != nil {\n", p.Title, p.FilePath, p.VarName)
				fmt.Fprintf(&b, "\t\t\thttp.Error(w, %q, http.StatusInternalServerError)\n", "render error")
				fmt.Fprintf(&b, "\t\t}\n")
				fmt.Fprintf(&b, "\t}))\n")
			}
		}

		b.WriteString("}\n")
	}

	return writeIfChanged(filepath.Join(themeDir, "pages_gen.go"), []byte(b.String()))
}

func discoverPlatformOverrides(dir string) ([]string, error) {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return nil, fmt.Errorf("read %s: %w", dir, err)
	}

	found := map[string]bool{}
	for _, entry := range entries {
		if entry.IsDir() || !strings.HasSuffix(entry.Name(), ".templ") {
			continue
		}

		data, err := os.ReadFile(filepath.Join(dir, entry.Name())) //nolint:gosec // path from ReadDir
		if err != nil {
			return nil, fmt.Errorf("read %s: %w", entry.Name(), err)
		}

		for _, m := range templFuncRe.FindAllStringSubmatch(string(data), -1) {
			if _, ok := platformVarFor[m[1]]; ok {
				found[m[1]] = true
			}
		}
	}

	var out []string
	for _, name := range platformOverrideable {
		if found[name] {
			out = append(out, name)
		}
	}

	return out, nil
}

func writeRegister(themeName, dir, module string, overrides []string) error {
	var b strings.Builder
	b.WriteString(generatedHeader)
	b.WriteString("package httpx\n\n")
	b.WriteString("import (\n")
	fmt.Fprintf(&b, "\tplatformhttpx %q\n", module+"/internal/platform/httpx")
	if themeName != defaultThemeName {
		fmt.Fprintf(&b, "\t_ %q\n", module+"/themes/default/platform/httpx")
	}
	b.WriteString(")\n\n")
	b.WriteString("func init() {\n")
	for _, name := range overrides {
		fmt.Fprintf(&b, "\tplatformhttpx.%s = %s\n", platformVarFor[name], name)
	}
	b.WriteString("}\n")

	return writeIfChanged(filepath.Join(dir, "register.go"), []byte(b.String()))
}

func writeAllThemeConfigs(themesDir, outDir string) (int, error) {
	entries, err := os.ReadDir(themesDir)
	if err != nil {
		if os.IsNotExist(err) {
			return 0, nil
		}

		return 0, fmt.Errorf("read themes dir %s: %w", themesDir, err)
	}

	specs, err := collectThemeConfigSpecs(themesDir, entries)
	if err != nil {
		return 0, err
	}

	if len(specs) == 0 {
		return 0, nil
	}

	merged, err := mergeShapes(specs)
	if err != nil {
		return 0, fmt.Errorf("union theme configs: %w", err)
	}

	types := finalize(merged)
	source := emitConfigGen("default", types)

	outPath := filepath.Join(outDir, "config_default_gen.go")
	if err := writeIfChanged(outPath, []byte(source)); err != nil {
		return 0, fmt.Errorf("write %s: %w", outPath, err)
	}

	return 1, nil
}

func collectThemeConfigSpecs(themesDir string, entries []os.DirEntry) ([]*TypeSpec, error) {
	specs := []*TypeSpec{}
	for _, entry := range entries {
		if !entry.IsDir() {
			continue
		}

		themeName := entry.Name()
		configPath := filepath.Join(themesDir, themeName, "config.yml")

		data, err := os.ReadFile(configPath) //nolint:gosec // path joined from fixed themes dir + entry name
		if err != nil {
			if os.IsNotExist(err) {
				continue
			}

			return nil, fmt.Errorf("theme %s: read config.yml: %w", themeName, err)
		}

		spec, err := inferFromYAML(data)
		if err != nil {
			return nil, fmt.Errorf("theme %s: infer config: %w", themeName, err)
		}

		specs = append(specs, spec)
	}

	return specs, nil
}
